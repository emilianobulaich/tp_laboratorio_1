
#ifndef OPERACIONES_H_
#define OPERACIONES_H_

/// \fn Suma()
/// \brief la funcion realiza la suma de 2 numeros flotantes
/// \param primer numero, segundo numero
/// \return retorna un flotante que es la suma de los 2 numeros ingresados
float Suma(float numero, float numero2);

/// \fn Resta()
/// \brief la funcion realiza la resta de 2 numeros flotantes
/// \param primer numero, segundo numero
/// \return retorna un flotante que es la resta de los 2 numeros ingresados
float Resta(float numero, float numero2);

/// \fn Multiplicacion()
/// \brief la funcion realiza la multiplicacion de 2 numeros flotantes
/// \param primer numero, segundo numero
/// \return retorna un flotante que es la multiplicacion de los 2 numeros ingresados
float Multiplicacion(float numero, float numero2);

/// \fn Division()
/// \brief la funcion realiza la division de 2 numeros flotantes
/// \param primer numero, segundo numero
/// \return retorna un flotante que es la division de los 2 numeros ingresados
float Division(float numero, float numero2);

/// \fn Factorial()
/// \brief la funcion realiza el factorial de un numero
/// \param un numero
/// \return retorna un flotante que es el factorial del numero ingresado
float Factorial(float numero);



#endif /* OPERACIONES_H_ */
